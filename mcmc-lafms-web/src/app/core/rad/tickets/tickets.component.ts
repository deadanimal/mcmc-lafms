  openModal(modalRef: TemplateRef<any>, row) {
    if (row) {
      console.log(row);
      this.editTicketForm.patchValue(row);
    }
    // this.modal = this.modalService.show(
    //   modalRef,
    //   Object.assign({}, { class: "gray modal-xl" })
    // );
    this.modal = this.modalService.show(modalRef, this.modalConfig);
  }

  closeModal() {
    this.modal.hide();
    this.editTicketForm.reset();
  }

  confirm() {
    swal
      .fire({
        title: "Confirmation",
        text: "Are you sure to create this new Ticket?",
        type: "info",
        buttonsStyling: false,
        confirmButtonClass: "btn btn-info",
        confirmButtonText: "Confirm",
        showCancelButton: true,
        cancelButtonClass: "btn btn-danger",
        cancelButtonText: "Cancel",
      })
      .then((result) => {
        if (result.value) {
          this.register();
        }
      });
  }

  register() {
    swal
      .fire({
        title: "Success",
        text: "A new Ticket has been created!",
        type: "success",
        buttonsStyling: false,
        confirmButtonClass: "btn btn-success",
        confirmButtonText: "Close",
      })
      .then((result) => {
        if (result.value) {
          this.modal.hide();
          this.editTicketForm.reset();
        }
      });
  }

  getChart() {
    // let chart = am4core.create("chartdivTicket", am4charts.XYChart);
    // let chart = am4core.create("chartdivTicket", am4charts.XYChart);
    let chart = am4core.create("chartdivTicket", am4charts.XYChart3D);

    // Add data
    chart.data = [
      {
        year: 2005,
        income: 23.5,
        color: chart.colors.next(),
      },
      {
        year: 2006,
        income: 26.2,
        color: chart.colors.next(),
      },
      {
        year: 2007,
        income: 30.1,
        color: chart.colors.next(),
      },
      {
        year: 2008,
        income: 29.5,
        color: chart.colors.next(),
      },
      {
        year: 2009,
        income: 24.6,
        color: chart.colors.next(),
      },
    ];

    // Create axes
    let categoryAxis = chart.yAxes.push(new am4charts.CategoryAxis());
    categoryAxis.dataFields.category = "year";
    categoryAxis.numberFormatter.numberFormat = "#";
    categoryAxis.renderer.inversed = true;

    let valueAxis = chart.xAxes.push(new am4charts.ValueAxis());

    // Create series
    let series = chart.series.push(new am4charts.ColumnSeries3D());
    series.dataFields.valueX = "income";
    series.dataFields.categoryY = "year";
    series.name = "Income";
    series.columns.template.propertyFields.fill = "color";
    series.columns.template.tooltipText = "{valueX}";
    series.columns.template.column3D.stroke = am4core.color("#fff");
    series.columns.template.column3D.strokeOpacity = 0.2;
  }
